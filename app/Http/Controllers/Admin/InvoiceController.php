    public function aging(Request $request)
    {
        BillingService::syncStatuses();

        $q = trim((string) $request->input('q', ''));
        $bucket = trim((string) $request->input('bucket', ''));
        $status = trim((string) $request->input('status', ''));
        $period = trim((string) $request->input('period', ''));

        $today = now()->startOfDay();
        $openStatuses = BillingService::openStatuses();

        $query = Invoice::query()
            ->with(['customer.package', 'customer.odpMaster', 'package'])
            ->whereIn('status', $openStatuses)
            ->when($period !== '', function ($query) use ($period) {
                $query->where('period', $period);
            })
            ->when($status !== '', function ($query) use ($status) {
                $query->where('status', $status);
            })
            ->when($q !== '', function ($query) use ($q) {
                $query->where(function ($sub) use ($q) {
                    $sub->where('invoice_number', 'like', "%{$q}%")
                        ->orWhere('period', 'like', "%{$q}%")
                        ->orWhereHas('customer', function ($customer) use ($q) {
                            $customer->where('name', 'like', "%{$q}%")
                                ->orWhere('phone', 'like', "%{$q}%")
                                ->orWhere('address', 'like', "%{$q}%")
                                ->orWhere('odp', 'like', "%{$q}%")
                                ->orWhere('pppoe_username', 'like', "%{$q}%");
                        });
                });
            });

        $allRows = $query
            ->orderBy('due_date')
            ->orderBy('customer_id')
            ->get()
            ->map(function (Invoice $invoice) use ($today) {
                $dueDate = Carbon::parse($invoice->due_date)->startOfDay();
                $days = $dueDate->diffInDays($today, false);

                if ($days < 0) {
                    $bucket = 'not_due';
                    $bucketLabel = 'Belum Jatuh Tempo';
                    $bucketClass = 'blue';
                } elseif ($days === 0) {
                    $bucket = 'today';
                    $bucketLabel = 'Jatuh Tempo Hari Ini';
                    $bucketClass = 'yellow';
                } elseif ($days <= 30) {
                    $bucket = '1_30';
                    $bucketLabel = 'Lewat 1–30 Hari';
                    $bucketClass = 'yellow';
                } elseif ($days <= 60) {
                    $bucket = '31_60';
                    $bucketLabel = 'Lewat 31–60 Hari';
                    $bucketClass = 'red';
                } else {
                    $bucket = 'over_60';
                    $bucketLabel = 'Lewat >60 Hari';
                    $bucketClass = 'red';
                }

                $invoice->aging_days = $days;
                $invoice->aging_bucket = $bucket;
                $invoice->aging_label = $bucketLabel;
                $invoice->aging_class = $bucketClass;
                $invoice->remaining_amount = max(0, (int) $invoice->amount - (int) $invoice->paid_amount);

                return $invoice;
            });

        $stats = [
            'total_count' => $allRows->count(),
            'total_amount' => $allRows->sum('remaining_amount'),

            'not_due_count' => $allRows->where('aging_bucket', 'not_due')->count(),
            'not_due_amount' => $allRows->where('aging_bucket', 'not_due')->sum('remaining_amount'),

            'today_count' => $allRows->where('aging_bucket', 'today')->count(),
            'today_amount' => $allRows->where('aging_bucket', 'today')->sum('remaining_amount'),

            '1_30_count' => $allRows->where('aging_bucket', '1_30')->count(),
            '1_30_amount' => $allRows->where('aging_bucket', '1_30')->sum('remaining_amount'),

            '31_60_count' => $allRows->where('aging_bucket', '31_60')->count(),
            '31_60_amount' => $allRows->where('aging_bucket', '31_60')->sum('remaining_amount'),

            'over_60_count' => $allRows->where('aging_bucket', 'over_60')->count(),
            'over_60_amount' => $allRows->where('aging_bucket', 'over_60')->sum('remaining_amount'),

            'belum_bayar_count' => $allRows->where('status', 'Belum Bayar')->count(),
            'nunggak_count' => $allRows->where('status', 'Nunggak')->count(),
        ];

        if ($bucket !== '') {
            $allRows = $allRows->where('aging_bucket', $bucket)->values();
        }

        $rows = $allRows->values();

        $periods = Invoice::query()
            ->select('period')
            ->distinct()
            ->orderByDesc('period')
            ->limit(24)
            ->pluck('period');

        return view('admin.invoices.aging', compact('rows', 'stats', 'q', 'bucket', 'status', 'period', 'periods'));
    }


    public function schedule(Request $request)
    {
        BillingService::syncStatuses();

        $period = BillingService::cleanPeriod($request->input('period') ?: now()->format('Y-m'));
        $bucket = trim((string) $request->input('bucket', ''));
        $q = trim((string) $request->input('q', ''));

        $today = now()->startOfDay();

        $customers = Customer::query()
            ->with(['package', 'odpMaster'])
            ->when($q !== '', function ($query) use ($q) {
                $query->where(function ($sub) use ($q) {
                    $sub->where('name', 'like', "%{$q}%")
                        ->orWhere('phone', 'like', "%{$q}%")
                        ->orWhere('address', 'like', "%{$q}%")
                        ->orWhere('odp', 'like', "%{$q}%")
                        ->orWhere('pppoe_username', 'like', "%{$q}%");
                });
            })
            ->orderByRaw('CASE WHEN billing_day IS NULL THEN 99 ELSE billing_day END')
            ->orderBy('name')
            ->get();

        $rows = $customers->map(function (Customer $customer) use ($period, $today) {
            $billingDay = (int) ($customer->billing_day ?: 1);
            $dueDateString = BillingService::dueDate($period, $billingDay);
            $dueDate = Carbon::parse($dueDateString)->startOfDay();

            $existing = Invoice::query()
                ->where('customer_id', $customer->id)
                ->where('period', $period)
                ->first();

            $amountRaw = $customer->monthly_price;
            $amount = (int) $amountRaw;

            $state = 'Akan Datang';
            $stateClass = 'blue';
            $bucket = 'upcoming';
            $canAutoGenerate = false;

            if ($customer->status !== 'active') {
                $state = 'Nonaktif';
                $stateClass = 'red';
                $bucket = 'inactive';
            } elseif ($amountRaw === null || $amountRaw === '' || $amount < 0) {
                $state = 'Nominal Kosong';
                $stateClass = 'yellow';
                $bucket = 'no_price';
            } elseif ($amount === 0) {
                $state = 'Gratis';
                $stateClass = 'blue';
                $bucket = 'free';
            } elseif ($existing) {
                $state = 'Sudah Ada - '.$existing->status;
                $stateClass = match ($existing->status) {
                    'Lunas' => 'green',
                    'Bayar Awal' => 'blue',
                    'Nunggak' => 'red',
                    default => 'yellow',
                };
                $bucket = 'existing';
            } elseif ($dueDate->isSameDay($today)) {
                $state = 'Jatuh Tempo Hari Ini';
                $stateClass = 'green';
                $bucket = 'today';
                $canAutoGenerate = true;
            } elseif ($dueDate->lt($today)) {
                $state = 'Terlewat Belum Dibuat';
                $stateClass = 'red';
                $bucket = 'overdue';
                $canAutoGenerate = false;
            } else {
                $state = 'Menunggu Tanggal Billing';
                $stateClass = 'blue';
                $bucket = 'upcoming';
            }

            return (object) [
                'customer' => $customer,
                'period' => $period,
                'billing_day' => $billingDay,
                'due_date' => $dueDate,
                'days_left' => $today->diffInDays($dueDate, false),
                'amount' => $amount,
                'existing' => $existing,
                'state' => $state,
                'state_class' => $stateClass,
                'bucket' => $bucket,
                'can_auto_generate' => $canAutoGenerate,
            ];
        });

        $stats = [
            'total' => $rows->count(),
            'today' => $rows->where('bucket', 'today')->count(),
            'upcoming' => $rows->where('bucket', 'upcoming')->count(),
            'overdue' => $rows->where('bucket', 'overdue')->count(),
            'existing' => $rows->where('bucket', 'existing')->count(),
            'free' => $rows->where('bucket', 'free')->count(),
            'inactive' => $rows->where('bucket', 'inactive')->count(),
            'no_price' => $rows->where('bucket', 'no_price')->count(),
            'today_amount' => $rows->where('bucket', 'today')->sum('amount'),
            'upcoming_amount' => $rows->where('bucket', 'upcoming')->sum('amount'),
        ];

        if ($bucket !== '') {
            $rows = $rows->where('bucket', $bucket)->values();
        }

        return view('admin.invoices.schedule', compact('period', 'bucket', 'q', 'rows', 'stats'));
    }


