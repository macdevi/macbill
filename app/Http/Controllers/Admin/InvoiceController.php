    public function preview(Request $request)
    {
        $period = BillingService::cleanPeriod($request->input('period'));
        $rows = BillingService::preview($period);

        $stats = [
            'ready' => $rows->where('can_generate', true)->count(),
            'existing' => $rows->filter(fn ($r) => (bool) $r->existing)->count(),
            'no_price' => $rows->filter(fn ($r) => !$r->existing && $r->customer->status === 'active' && $r->amount <= 0)->count(),
            'inactive' => $rows->filter(fn ($r) => $r->customer->status !== 'active')->count(),
            'total_preview' => $rows->where('can_generate', true)->sum('amount'),
        ];

        return view('admin.invoices.preview', compact('period', 'rows', 'stats'));
    }

    public function generateSelected(Request $request)
    {
        $period = BillingService::cleanPeriod($request->input('period'));
        $ids = (array) $request->input('customer_ids', $request->input('selected', []));

        
        $invoiceBase = $request->is('collector/*') ? '/collector/invoices' : '/admin/invoices';

        if (!$ids) {
            return redirect($invoiceBase . '/preview?period=' . urlencode($period))
                ->with('error', 'Belum ada pelanggan yang dicentang.');
        }

        $result = BillingService::generateSelected($period, $ids);

        $message = 'Generate selesai. Dibuat: ' . $result['created'] .
            ', sudah ada: ' . $result['existing'] .
            ', nonaktif: ' . $result['inactive'] .
            ', gratis: ' . ($result['free_customer'] ?? 0) .
            ', nominal kosong: ' . $result['no_price'] . '.';

        if (!empty($result['errors'])) {
            $message .= ' Error: ' . implode(' | ', array_slice($result['errors'], 0, 3));
        }

        return redirect($invoiceBase . '/preview?period=' . urlencode($period))
            ->with($result['created'] > 0 ? 'success' : 'error', $message);
    }

    public function index(Request $request)
    {
        BillingService::syncStatuses();

        $period = BillingService::cleanPeriod($request->input('period'));
        $status = $request->input('status');

        $invoices = Invoice::with(['customer', 'package'])
            ->when($period, fn ($q) => $q->where('period', $period))
            ->when($status, fn ($q) => $q->where('status', $status))
            ->latest()
            ->paginate(30)
            ->withQueryString();

        return view('admin.invoices.index', compact('invoices', 'period', 'status'));
    }

