    public function resetCustomerInvoiceData(Request $request, \App\Models\Customer $customer)
    {
        $confirm = trim((string) $request->input('confirm_reset_customer'));

        if ($confirm !== 'RESET TAGIHAN PELANGGAN') {
            return back()->with('error', 'Reset dibatalkan. Kode konfirmasi tidak valid.');
        }

        $backupPath = null;

        try {
            $dbPath = config('database.connections.sqlite.database');

            if ($dbPath && is_string($dbPath) && file_exists($dbPath)) {
                $backupDir = storage_path('app/backups');

                if (!is_dir($backupDir)) {
                    mkdir($backupDir, 0775, true);
                }

                $safeName = preg_replace('/[^A-Za-z0-9_\-]/', '_', (string) $customer->name);
                $backupPath = $backupDir . '/database-before-reset-customer-' . $customer->id . '-' . $safeName . '-' . now()->format('Ymd_His') . '.sqlite';

                copy($dbPath, $backupPath);
            }
        } catch (\Throwable $e) {
            return back()->with('error', 'Backup database gagal dibuat. Reset dibatalkan: ' . $e->getMessage());
        }

        $invoiceCount = 0;
        $paymentCount = 0;

        try {
            \Illuminate\Support\Facades\DB::transaction(function () use ($customer, &$invoiceCount, &$paymentCount) {
                $invoiceIds = \App\Models\Invoice::where('customer_id', $customer->id)->pluck('id')->all();

                $paymentQuery = \App\Models\Payment::where(function ($q) use ($customer, $invoiceIds) {
                    $q->where('customer_id', $customer->id);

                    if (!empty($invoiceIds)) {
                        $q->orWhereIn('invoice_id', $invoiceIds);
                    }
                });

                $paymentCount = (clone $paymentQuery)->count();
                $paymentQuery->delete();

                $invoiceQuery = \App\Models\Invoice::where('customer_id', $customer->id);
                $invoiceCount = (clone $invoiceQuery)->count();
                $invoiceQuery->delete();
            });
        } catch (\Throwable $e) {
            return back()->with('error', 'Reset data tagihan pelanggan gagal: ' . $e->getMessage());
        }

        $msg = 'Data tagihan pelanggan ' . $customer->name . ' berhasil direset. Invoice dihapus: ' . $invoiceCount . ', payment dihapus: ' . $paymentCount . '.';

        if ($backupPath) {
            $msg .= ' Backup database: ' . $backupPath;
        }

        return back()->with('success', $msg);
    }

    public function resetInvoice(Request $request, Invoice $invoice)
    {
        $reason = trim((string) $request->input('reason', 'Reset invoice oleh admin'));

        \Illuminate\Support\Facades\DB::transaction(function () use ($invoice, $reason) {
            \App\Models\Payment::where('invoice_id', $invoice->id)->delete();

            $oldNotes = trim((string) $invoice->notes);
            $resetNote = '[RESET ADMIN ' . now()->format('Y-m-d H:i:s') . '] ' . $reason;

            $invoice->update([
                'paid_amount' => 0,
                'paid_at' => null,
                'payment_method' => null,
                'status' => 'Belum Bayar',
                'notes' => $oldNotes !== '' ? $oldNotes . "\n" . $resetNote : $resetNote,
            ]);
        });

        return back()->with('success', 'Invoice ' . $invoice->invoice_number . ' berhasil direset menjadi Belum Bayar.');
    }

